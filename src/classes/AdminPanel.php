class AdminPanel
{

	private $j;

	private $isfile = false;

	public function __construct(){

		$file = CurrentUser::$path;
		if(!is_array($file) && is_file($file)){
			$this->isfile = true;
		}

		$this->j = new Judge($file);
	}

	public function toHTML(){



		if(CurrentUser::$admin){
			echo $this->j->toHTML();
		}

	}
}


